#<<---Basic Math operations--->>#

#--> Variables to operate
a = 10
b = 3

#<<---Addition--->>#
result = a + b #Classical Addition: A plus B
print(f"Sum: {result}")

#<<---Substraction--->>#
result = a - b #Classical Substraction: A minus B
print(f"Substraction: {result}")

#<<---Multiplication--->>#
result = a * b #Classical Multiplication: A by B
print(f"Mutliplication: {result}")

#<<---Division--->>#
result = a / b #Classical Division: A divided by B
print(f"Division: {result}")
result = a // b #Integer part of a division
print(f"Integer division: {result}")

#<<---Module--->>#
result = a % b #Residue of a Division
print(f"Module: {result}")

#<<---Power--->>#
result = a ** b #Power a number by itself
print(f"Power: {result}")

#<<---Comparation symbols--->>#
print(a > b) #Bigger than B
print(a < b) #Smaller than B
print(a >= b) #Bigger or equal to B
print(a <= b) #Smaller or equal to B
print(a == b) #Equal to B
print(a != b) #Different from B
